function roundedRect(x, y, width, height, radius) {
    ctx.beginPath();
    ctx.moveTo(x + radius, y);
    ctx.arcTo(x + width, y, x + width, y + height, radius);
    ctx.arcTo(x + width, y + height, x, y + height, radius);
    ctx.arcTo(x, y + height, x, y, radius);
    ctx.arcTo(x, y, x + width, y, radius);
    ctx.closePath();
    ctx.fill();
}


gameStart()


function gameStart() {
    running = true
    scoreText.textContent = score
    createFood()
    drawFood()
    nextTick()
}

function nextTick() {
    if (running) {
        setTimeout(() => {
            clearBoard();
            drawFood()
            moveSnake()
            drawSnake()
            checkGameOver()
            nextTick()
        }, 75)
    } else {
        displayGameOver()
    }
}

function clearBoard() {
    ctx.fillStyle = boardBackground;
    ctx.fillRect(0, 0, gameWidth, gameHeight)
}

function createFood() {
    function randomFood(min, max) {
        const randNum = Math.round((Math.random() * (max - min) + min) / unitSize) * unitSize
        return randNum
    }

    foodX = randomFood(0, gameWidth - unitSize)
    foodY = randomFood(0, gameWidth - unitSize)
}

function drawFood() {
    ctx.fillStyle = boardBackground;
    ctx.fillStyle = foodColor
    roundedRect(foodX, foodY, unitSize, unitSize, 13)
}

function moveSnake() {
    const head = {x: snake[0].x + xVelocity, y: snake[0].y + yVelocity}
    snake.unshift(head)
    if (snake[0].x === foodX && snake[0].y === foodY) {
        score += 1
        scoreText.textContent = score
        createFood()
        fetch(baseURL, {
            method: "POST",
            body: JSON.stringify({highScore: score,name:playerName}),
            headers: {
                "Content-Type": "application/json",
            },
        }).then(res => {
            console.log('sucess')
        })
    } else {
        snake.pop()
    }
}

function drawSnake() {
    ctx.fillStyle = snakeColor
    ctx.strokeStyle = snakeBorder
    snake.forEach((snakePart, i) => {
        if (i === 0) {
            roundedRect(snakePart.x, snakePart.y, unitSize, unitSize, 20)
        } else {
            roundedRect(snakePart.x, snakePart.y, unitSize, unitSize, 8)
        }
    })
}

function changeDirection(event) {
    const keyPress = event.keyCode
    const LEFT = 37
    const UP = 38
    const RIGHT = 39
    const DOWN = 40

    const goingUp = (yVelocity === -unitSize)
    const goingDown = (yVelocity === unitSize)
    const goingRight = (xVelocity === unitSize)
    const goingLeft = (xVelocity === -unitSize)

    switch (true) {
        case(keyPress === LEFT && !goingRight):
            xVelocity = -unitSize
            yVelocity = 0
            break;
        case(keyPress === UP && !goingDown):
            xVelocity = 0
            yVelocity = -unitSize
            break;
        case(keyPress === RIGHT && !goingLeft):
            xVelocity = unitSize
            yVelocity = 0
            break;
        case(keyPress === DOWN && !goingUp):
            xVelocity = 0
            yVelocity = unitSize
            break;
    }

}

function checkGameOver() {
    switch (true) {
        case (snake[0].x < 0):
            running = false
            break
        case (snake[0].x >= gameWidth):
            running = false
            break
        case (snake[0].y < 0):
            running = false
            break
        case (snake[0].y >= gameHeight):
            running = false
            break
    }
    for (let i = 1; i < snake.length; i++) {
        if (snake[i].x === snake[0].x && snake[i].y === snake[0].y) {
            running = false
        }
    }

}

function displayGameOver() {
    ctx.font = "50px MV Boli"
    ctx.fillStyle = 'black'
    ctx.textAlign = "center"
    ctx.fillText("GAME OVER !", gameWidth / 2, gameHeight / 2)
    running = false
}

function resetGame() {
    score = 0
    xVelocity = unitSize
    yVelocity = 0
    snake = [
        {x: unitSize * 4, y: 0},
        {x: unitSize * 3, y: 0},
        {x: unitSize * 2, y: 0},
        {x: unitSize, y: 0},
        {x: 0, y: 0}
    ]
    gameStart()
}



